public class basic_01字串 {
    public static void main(String[] args) {
        for (int a = 0; a <= 1; a++) {
            for (int b = 0; b <= 1; b++) {
                for (int c = 0; c <= 1; c++) {
                    for (int d = 0; d <= 1; d++) {
                        for (int e = 0; e <= 1; e++) {
                            System.out.println("" + a + b + c + d + e);
                        }
                    }
                }
            }
        }
    }
}
